<template>
  <div class="grid grid-cols-1 sm:grid-cols-2 md:grid-cols-2 lg:grid-cols-3 xl:grid-cols-4 gap-4 sm:gap-6">
    <Card 
      v-for="tour in tours" 
      :key="tour.id"
      class="relative overflow-hidden shadow-lg border-0 rounded-lg transform transition-all duration-300 hover:scale-105 hover:shadow-xl cursor-pointer group h-[360px] flex flex-col"
      @click="verTour(tour)"
    >
      <!-- Badge de estado en esquina superior derecha con z-index m√°s alto -->
      <div 
        :class="[
          'absolute bottom-2 right-2 z-10 px-2 py-1 rounded-full text-xs font-semibold shadow-lg',
          obtenerEstadoInfo(tour.estado, tour.cupos_disponibles).color
        ]"
      >
        {{ obtenerEstadoInfo(tour.estado, tour.cupos_disponibles).texto }}
      </div>

      <!-- Badge "destacado" si aplica -->
      <div 
        v-if="tour.destacado"
        class="absolute top-2 left-2 z-10 bg-gradient-to-r from-yellow-400 to-orange-500 text-black px-2 py-1 rounded-full text-xs font-bold shadow-lg"
      >
        ‚≠ê DESTACADO
      </div>

      <!-- Imagen principal con carrusel autom√°tico -->
      <div class="relative h-48 overflow-hidden group">
        <!-- Imagen -->
        <img 
          :src="obtenerImagenActual(tour)" 
          :alt="tour.nombre"
          class="w-full h-full object-cover transition-transform duration-500 group-hover:scale-110"
          @error="(e) => { console.log('Error imagen:', e, tour); e.target.src = 'https://via.placeholder.com/400x300/2563eb/ffffff?text=Sin+Imagen' }"
        />

        <!-- Indicadores del carrusel en la parte inferior centrados -->
        <div 
          v-if="tour.imagenes && tour.imagenes.length > 1"
          class="absolute bottom-2 left-1/2 transform -translate-x-1/2 flex space-x-1 z-10"
        >
          <div
            v-for="(_, index) in tour.imagenes"
            :key="index"
            :class="[
              'w-2 h-2 rounded-full transition-all duration-300',
              (cardImageIndices[tour.id] || 0) === index 
                ? 'bg-white scale-125' 
                : 'bg-white/50'
            ]"
          ></div>
        </div>

        <!-- Bot√≥n para ver galer√≠a de im√°genes -->
        <button
          v-if="tour.imagenes && tour.imagenes.length > 1"
          @click.stop="verGaleria(tour)"
          class="absolute top-2 right-2 bg-black/50 text-white p-2 rounded-full opacity-0 group-hover:opacity-100 transition-opacity duration-300 hover:bg-black/70"
        >
          <i class="pi pi-images text-sm"></i>
        </button>
      </div>

      <!-- Contenido de la card -->
      <div class="p-4 flex flex-col flex-grow">
        <!-- T√≠tulo -->
        <h3 class="text-lg font-bold text-gray-800 mb-2 line-clamp-2 group-hover:text-blue-600 transition-colors">
          {{ tour.nombre }}
        </h3>

        <!-- Informaci√≥n del tour -->
        <div class="space-y-2 mb-4 flex-grow">
          <div class="flex items-center text-gray-600 text-sm">
            <i class="pi pi-calendar mr-2 text-blue-600"></i>
            <span>{{ formatearFecha(tour.fecha_salida) }}</span>
          </div>
          <div class="flex items-center text-gray-600 text-sm">
            <i class="pi pi-clock mr-2 text-blue-600"></i>
            <span>{{ calcularDuracion(tour.fecha_salida, tour.fecha_regreso) }}</span>
          </div>
          <div class="flex items-center text-gray-600 text-sm">
            <i class="pi pi-map-marker mr-2 text-blue-600"></i>
            <span class="truncate">{{ tour.punto_salida }}</span>
          </div>
          <div class="flex items-center text-gray-600 text-sm">
            <i class="pi pi-users mr-2 text-blue-600"></i>
            <span>{{ tour.cupos_disponibles }} cupos disponibles</span>
          </div>
        </div>

        <!-- Precio y bot√≥n -->
        <div class="mt-auto">
          <!-- Precio -->
          <div class="mb-3">
            <span class="text-2xl font-bold text-gray-800">
              ${{ parseFloat(tour.precio).toLocaleString('en-US', { minimumFractionDigits: 2 }) }}
            </span>
            <span class="text-gray-600 text-sm ml-1">por persona</span>
          </div>

          <!-- Botones -->
          <div class="flex gap-2">
            <Button
              :label="obtenerTextoBoton(tour)"
              @click.stop="reservarTour(tour)"
              :disabled="!esTourReservable(tour)"
              :class="[
                '!border-none !px-3 !py-2 !text-sm font-semibold rounded transition-all flex-1 shadow-sm',
                esTourReservable(tour)
                  ? tipoTour === 'nacional' 
                    ? '!bg-red-600 !text-white hover:!bg-red-700' 
                    : '!bg-blue-600 !text-white hover:!bg-blue-700'
                  : '!bg-gray-400 !text-white cursor-not-allowed'
              ]"
              size="small"
            />
            <Button
              label="Info"
              @click.stop="verTour(tour)"
              outlined
              :class="[
                '!px-3 !py-2 !text-sm font-semibold rounded hover:!bg-gray-50 transition-all',
                tipoTour === 'nacional' 
                  ? '!border-red-600 !text-red-600' 
                  : '!border-blue-600 !text-blue-600'
              ]"
              size="small"
            />
          </div>
        </div>
      </div>
    </Card>
  </div>
</template>

<script setup>
import { onMounted, onUnmounted, watch } from 'vue'
import Card from 'primevue/card'
import Button from 'primevue/button'
import { router } from '@inertiajs/vue3'

const props = defineProps({
  tours: {
    type: Array,
    required: true
  },
  tipoTour: {
    type: String,
    required: true,
    validator: value => ['nacional', 'internacional'].includes(value)
  },
  // Funciones pasadas desde el composable
  obtenerEstadoInfo: {
    type: Function,
    required: true
  },
  esTourReservable: {
    type: Function,
    required: true
  },
  obtenerTextoBoton: {
    type: Function,
    required: true
  },
  formatearFecha: {
    type: Function,
    required: true
  },
  calcularDuracion: {
    type: Function,
    required: true
  },
  obtenerImagenActual: {
    type: Function,
    required: true
  },
  reservarTour: {
    type: Function,
    required: true
  },
  verGaleria: {
    type: Function,
    required: true
  },
  inicializarCarrusel: {
    type: Function,
    required: true
  },
  detenerTodosLosCarruseles: {
    type: Function,
    required: true
  },
  cardImageIndices: {
    type: Object,
    required: true
  }
})

// Funci√≥n para ver tour completo
const verTour = (tour) => {
  const ruta = props.tipoTour === 'nacional' 
    ? `/detalle-tour-nacional/${tour.id}` 
    : `/detalle-tour-internacional/${tour.id}`
  router.visit(ruta)
}

// Watcher para debuggear tours recibidos
watch(() => props.tours, (newTours) => {
  console.log('üéØ ToursGrid recibi√≥ tours:', newTours.length)
  console.log('üìã Lista de tours:', newTours)
}, { immediate: true })

// Lifecycle hooks
onMounted(() => {
  // Inicializar carruseles para todos los tours con m√∫ltiples im√°genes
  props.tours.forEach(tour => {
    if (tour.imagenes && tour.imagenes.length > 1) {
      props.inicializarCarrusel(tour)
    }
  })
})

onUnmounted(() => {
  // Limpiar todos los intervalos al desmontar el componente
  props.detenerTodosLosCarruseles()
})
</script>

